    if (val == null || val.isEmpty) {
      return FFLocalizations.of(context).getText(
        'gx4x0ty4' /* Password is required */,
      );
    }

    if (val.length < 8) {
      return FFLocalizations.of(context).getText(
        'j2r428cm' /* Minimum 8 characters */,
      );
    }

    return null;
  }

  // State field(s) for confirmPassword widget.
